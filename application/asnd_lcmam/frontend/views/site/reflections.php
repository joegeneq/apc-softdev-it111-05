				<b>Second Reading:1 Cor. 12:3b-7, 12-131</b>
			</h3>

			<h4 style="text-decoration: underline; text-indent: 20px; font-weight: bold">BACKGROUND/CONTEXT OF THE READING</h4>
			<p style="margin-left: 20px" align="justify">
			The church in Corinth, composed of mostly Jewish and Greek converts, faced many issues – internal divisions and questions on faith. Paul writes to the Corinthians to address the problems encountered by the young church.
			</p>
			
			<br>

			<h4 style="text-decoration: underline; text-indent: 20px; font-weight: bold">HOW TO PROCLAIM</h4>
			<p style="margin-left: 20px" align="justify">
			Pause briefly between Paul’s expressions of difference and sameness. Look up the assembly and proclaim clearly and solemnly, emphasizing each word of the phrase, “so also Christ”. Emphasize each occurrence of the word,”one” to drive home Paul’s support and encouragement for unity in the Church.
			</p>

			<br>

			<h3 style="text-indent: 20px">
				<b>Gospel: Gospel John 20:19-23</b>
			</h3>

			<h4 style="text-decoration: underline; text-indent: 20px; font-weight: bold">BACKGROUND/CONTEXT OF THE READING</h4>
			<p style="margin-left: 20px" align="justify">
			After Jesus’ crucifixion, his disciples were afraid for their lives. Their master is dead and they fear that the authorities would be looking for them next.
			</p>
			
			<br>

			<h4 style="text-decoration: underline; text-indent: 20px; font-weight: bold">MESSAGE/MEANING OF THE READING</h4>
			<p style="margin-left: 20px" align="justify">
			Jesus’ first words to his disciples were “Peace be with you.” He knew that they were afraid and anxious. The disciples rejoiced and Jesus reiterated his greeting, “Peace be with you”, gave them their mission as He breathed the Holy Spirit in them.  
			<br><br>
			“Peace be with you” is a normal Jewish greeting. But Jesus was not wishing his disciples peace; He was assuring them that they have peace. The disciples were told to continue the mission of Jesus - they are to continue doing what He did. And when Jesus breathed on his disciples the Holy Spirit, he breathed new life into them, as God breathed on the man he formed from clay and gave it life.
			<br><br>
			The power that came with the Holy Spirit was the authority to forgive. For the power of God, his Spirit, is a power for forgiveness, for bringing people back to God. The same mission is given to each of us. We are sent to help other people be reconciled to God, to work with God to build his Kingdom. And each of us has a role in this mission, which is why the Spirit has given us different gifts which we should use for the benefit of the one body of Christ, His Church.  
			</p>

			<br>

			<h4 style="text-decoration: underline; text-indent: 20px; font-weight: bold">REFLECTIONS</h4>
			<p style="margin-left: 20px" align="justify">
			What are your gifts? For whose benefit do you use your gifts? How can these be used for the benefit of God’s people? What evidence of the Spirit’s working in you can be observed? What fruits of the Spirit can be seen in your life?
			</p>
			

