			<h3 style="text-indent: 20px">
				<b>Gospel: Matthew 28:16-20</b>
			</h3>

			<h4 style="text-decoration: underline; text-indent: 20px; font-weight: bold">BACKGROUND/CONTEXT OF THE READING</h4>
			<p style="margin-left: 20px" align="justify">
			A few verses before today’s reading, Jesus warned his listeners about false prophets who draw attention to themselves with their pious practices. The Great Commission, the Gospel reading for today, takes place on a mountain during Jesus’ final appearance. Matthew places this scene in Galilee while Luke in Acts relates that Jesus’ appearance was in Jerusalem. The emphasis should not be on where Jesus appeared but on what Jesus had to say before he ascended to Heaven.</p>
			
			<br>

			<h4 style="text-decoration: underline; text-indent: 20px; font-weight: bold">MESSAGE/MEANING OF THE READING</h4>
			<p style="margin-left: 20px" align="justify">
			Jesus initially declares his authority from the Father. And then he gives his disciples his mission, as he passes his own authority to them.
			<br><br>
			The Ascension portrays a different kind of faith. This time, God entrusts man with the mission of working for the coming of God’s Kingdom. God knew that man, filled with His Spirit, will rise to the occasion and courageously carry out the commission.
			<br><br>
			Two thousand years after, we have the same duty to proclaim the Good News, in words and deeds. By the power of the Hoy Spirit, we are to continue the work of Jesus: teaching, healing, spreading love and forgiveness, promoting justice, and caring for the poor.
			</p>

