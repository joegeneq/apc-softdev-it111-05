			<h3 style="text-decoration: underline; font-weight: bold">Solemnity of Pentecost</h3>
			<h3 style="text-indent: 20px">
				<b>First Reading: Acts 2:1-11</b>
			</h3>

			<h4 style="text-decoration: underline; text-indent: 20px; font-weight: bold">BACKGROUND/CONTEXT OF THE READING</h4>
			<p style="margin-left: 20px" align="justify">
				The Jewish Pentecost was the second most important Jewish feast. Every year, on this day, devout Jews from all around the world gather to celebrate the birth of Israel as God’s chosen people, in the covenant Law given to Moses on Mt. Sinai. Jesus’ disciples gathered, not to celebrate the feast, but to wait for the fulfillment of a promise made by Jesus before He ascended into Heaven.
			</p>
			
			<br>

			<h4 style="text-decoration: underline; text-indent: 20px; font-weight: bold">MESSAGE/MEANING OF THE READING</h4>
			<p style="margin-left: 20px" align="justify">
			The timing was very fitting. Christ’s church was born on the feast day of God’s covenant with his chosen people! The birth of the chosen people became the birth of the church, the new people of God. The feast of the Law became the feast of the Spirit.
			<br><br>
			The Holy Spirit, unleashed on Jesus’ disciples, transformed a timid and anxious group into bold and powerful witnesses for Christ. From a confused and scattered lot, they were united into a community characterized by brotherly love and filled with a deep sense of mission. The same Spirit is the origin of all the great movements in the Church, transforming and renewing it in every generation.
			<br><br>
			We, too, receive the Holy Spirit in the sacraments - quietly, but just as powerfully. The Holy Spirit, who is completely and truly God now dwells within us, transforming us to become like Christ, transforming us to be God’s pride and joy. But to feel the transforming effect of the Holy Spirit in our lives, our cooperation is indispensable. We have to listen to the promptings of the Spirit and be open to Him. Mary Most Holy and all the saints are eloquent proofs of the transforming power of the Holy Spirit.
			</p>

			<br>

			<h4 style="text-decoration: underline; text-indent: 20px; font-weight: bold">HOW TO PROCLAIM</h4>
			<p style="margin-left: 20px" align="justify">
			Beginning with the words, “And suddenly,” build momentum in your proclamation through the words, “as the spirit enabled. . .”, by progressively increasing the energy in your voice as you are telling of this dramatic event. Pause before the words, “Now there were devout Jew. . .” Lower the tone of your voice and resume a narrative tone as you read this sentence. Practice the pronunciation of the words in Word Watch, This reading is both challenging and rewarding because of the concentration of Biblical Words in one sentence. Do not rush through the enumeration; enunciate each name in the list, to further emphasize to the assembly the universal reach of God’s saving work through Christ in the Holy Spirit. Proclaim the concluding line of the reading (“yet we hear them speaking”) without rushing and with marvel and excitement in your voice.
			</p>

