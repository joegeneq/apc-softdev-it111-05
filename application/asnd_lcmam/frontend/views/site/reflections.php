			<h4 style="text-decoration: underline; text-indent: 20px; font-weight: bold">RESPONSORIAL PSALM: LORD, SEND OUT YOUR SPIRIT AND RENEW THE FACE OF THE EARTH!</h4>
			<p style="margin-left: 20px" align="justify">
			The Psalmist praises God for his wonderful works in creation. When the psalmist talks about the Spirit renewing the earth, he was most probably referring to springtime. For Christians, the renewal of the face of the earth can be viewed as a renewal of creation with the church being its first fruits.
